//Este código es un ejemplo del uso de un metodo de la clase String (String charAt(index))
import java.lang.String;
class code{
    public static void main(String[] args){
        String S1 = "Hello";
        char ch = S1.charAt(2); //Este método retorna el char indexado al número 2 (letra l)
        System.out.println(ch);
    }
}