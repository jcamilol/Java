//Este código es un ejemplo del uso del switch...case statement usando enteros
class code{
    public static void main(String[] args){
        int n = 2;
        switch(n){
            case 1:
                System.out.println("El número es 1");
                break;
            case 2:
                System.out.println("El número es 2");
                break;
            default:
                System.out.println("El número es mayor de 2");
        }
    }
}