class code{
    public static void main(String[] args){
	int a=10, b=10, c=10, d=10;
	System.out.println("Value of a++ = "+(a++)); //Postfix ++ operator
	System.out.println("Value of ++b = "+(++b)); //Prefix ++ operator
	System.out.println("Value of c-- = "+(c--)); //Postfix -- operator
	System.out.println("Value of --d = "+(--d)); //Prefix -- operator
    }
}
