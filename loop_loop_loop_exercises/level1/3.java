// Print multiplication table of 24, 50 and 29 using loop. 
class code{
    public static void main(String[] args){
        System.out.println("Tabla del 24\n");
        for(int m2=1; m2<=10; m2++){
            System.out.println(24+"*"+m2+" = "+(24*m2));
        }
        System.out.println("\nTabla del 50\n");
        for(int m2=1; m2<=10; m2++){
            System.out.println(50+"*"+m2+" = "+(50*m2));
        }
        System.out.println("\nTabla del 29\n");
        for(int m2=1; m2<=10; m2++){
            System.out.println(29+"*"+m2+" = "+(29*m2));
        }
    }
}