//Este código es un ejemplo del uso de un método de la clase String (concat(String str))
import java.lang.String;
class code{
    public static void main(String[] args){
        String S1 = "Codes";
        String S2 = S1.concat("Dope"); //Este método "conecta" el String S1 ("Codes") con el String ("Dope"), y retorna el String unido S2
        System.out.println(S2);
    }
}