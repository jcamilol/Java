//Este código es un ejemplo del uso de un metodo de la clase String (int length())
import java.lang.String;
class code{
    public static void main(String[] args){
        String S1 = "Hello World";
        int l = S1.length(); //Este método retorna el tamaño del array, incluidos espacios
        System.out.println("El tamaño del String es "+l);
    }
}